const myArr = [10, 20, 30, 40, 50, 60];

//Get the elements from the specified index range.
console.log(myArr.slice(0, 2));

//Get the elements from the speficied index.
console.log(myArr.slice(2));

//Start from the end and get the elements with the given count.
console.log(myArr.slice(-2));

//Get the whole Array.
console.log(myArr.slice());